class Mother():
    def __init__(self):
        pass

    def eyes(self):
        print("My mother's eyes are green")

    def nose(self):
        print("My mother's nose is beautiful")


class Father():
    def __init__(self):
        pass

    def eyes(self):
        print("My father's eyes are brown")

    def nose(self):
        print("My father's nose is big")

moth = Mother()
moth.eyes()
moth = Father()
moth.eyes()